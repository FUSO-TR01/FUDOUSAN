package com.example.demo.fudo;

public class ChatInput {

	private String chat;
	private String name;

	public ChatInput() {

	}

	public String getChat() {
		return chat;
	}

	public void setChat(String chat) {
		this.chat = chat;
	}
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
