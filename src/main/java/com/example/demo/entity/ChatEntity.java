package com.example.demo.entity;

public class ChatEntity {
	
	private int id;
	private String chat;
	
	public ChatEntity() {

	}
	
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getChat() {
		return chat;
	}

	public void setChat(String chat) {
		this.chat = chat;
	}
	
}
